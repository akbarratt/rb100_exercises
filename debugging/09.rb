def digit_product(str_num)
  digits = str_num.chars.map { |n| n.to_i }
  product = 1

  digits.each do |digit|
    product *= digit
  end

  product
end


p digit_product('12345')
# expected return value: 120
# actual return value: 0

# Because product is initialized with a value of 0, each subsequent digit is being multiplied by zero. Product instead needs to be initialized with a value of 1.